#include<iostream>
#include<vector>
using namespace std;
int row, col, currIslarea, ans = 0;
    void dfs(vector<vector<int>>& binary_matrix, int x, int y) {
        if (x < 0 || y < 0 || x == row || y == col)
        {
        return;
        }
        int &currposn = binary_matrix[y][x];
        if (currposn != 1)  //checking if land is connected
        {
        return;
        }
        currposn = -1;
        currIslarea++;
        dfs(binary_matrix, x + 1, y);
        dfs(binary_matrix, x, y + 1);
        dfs(binary_matrix, x - 1, y);
        dfs(binary_matrix, x, y - 1);
    }
    int area(vector<vector<int>>& binary_matrix) {
        row = binary_matrix[0].size();
        col = binary_matrix.size();
        for (int y = 0; y < col; y++) {
            for (int x = 0; x < row; x++) {
                if (binary_matrix[y][x] == 1) {
                    currIslarea = 0;
                    dfs(binary_matrix, x, y);
                    ans = max(ans, currIslarea);
                }
            }
        }
        return ans;
}
int main()
{
    vector<vector<int>> binary_matrix ;
    int r, c;
    cout<<"The number of rows and columns : "<<endl;
    cout<<"Row: ";
    cin>>r;
    cout<<"Column: ";
    cin>>c;
    int data;
    for(int i=0; i < r; i ++)
    {
         vector<int> temp;
        for(int j=0; j < c ; j ++)
        {
            cin>>data;
            temp.push_back(data);
        }
        binary_matrix.push_back(temp);
        temp.clear();
    }
    cout<<"The area of island is : "<<area(binary_matrix);
    return 0;
}

